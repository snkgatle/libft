#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char *p_s1;

	p_s1 = s1;
	while (*p_s1)
		p_s1++;
	while (n-- > 0 && *s2)
		*p_s1++ = *s2++;
	*p_s1 = '\0';
	return (s1);
}
